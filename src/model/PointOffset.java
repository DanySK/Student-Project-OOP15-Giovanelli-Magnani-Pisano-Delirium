package model;

public class PointOffset {
    private final int offsetX;
    private final int offsetY;
    
    public PointOffset(int offsetX, int offsetY) {
        super();
        this.offsetX = offsetX;
        this.offsetY = offsetY;
    }
    public int getOffsetX() {
        return offsetX;
    }
    public int getOffsetY() {
        return offsetY;
    }
}
